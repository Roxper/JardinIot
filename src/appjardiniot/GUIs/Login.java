    public String[] getUsers(){     //Este metodo crea un arreglo con el numero de espacios igual al num de usuarios y los asigna en cada espacio.
        int i;
        String Users[];
        Users = new String[SR.getNumUsers()];
        for(i=0;i<Users.length;i++){
            Users[i] = SR.getUser(i).getNombre();
        }
        return Users;
    }
    
