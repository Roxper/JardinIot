    public void Leer(){    //Este metodo intenta leer el archivo de los usuarios creados
        ObjectInputStream In;
        try{
            In = new ObjectInputStream(new FileInputStream("Users.data"));
            SR = (SistRiego)In.readObject();        
            In.close();
            System.out.println("Archivo Cargado");

        }catch(FileNotFoundException e){
                    System.out.println("Archivo No Encontrado");                   
            }catch(IOException e){
                    System.out.println("No se puede Mostrar");                   
            } catch (ClassNotFoundException ex) {
                    System.out.println("Clase Erronea");                
            }  
    }

